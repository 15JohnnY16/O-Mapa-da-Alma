export const getLanguageFromCountry = (country: Country | string): Language => {
    if (!country) return "en";

    const ptCountries = ["BR", "PT", "AO", "MZ", "GW", "TL", "CV", "ST"];
    const esCountries = [
        "AR", "BO", "CL", "CO", "CR", "CU", "DO", "EC", "SV", "GQ", "GT", "HN",
        "MX", "NI", "PA", "PY", "PE", "ES", "UY", "VE"
    ];

    if (ptCountries.includes(country)) return "pt";
    if (esCountries.includes(country)) return "es";

    return "en";
};

export const getLanguageFromNavigator = (): Language | null => {
    if (typeof window === "undefined") return null;

    const navLang = navigator.language || navigator.languages?.[0];
    if (!navLang) return null;

    if (navLang.startsWith("pt")) return "pt";
    if (navLang.startsWith("es")) return "es";

    return null;
};
